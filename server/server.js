require('dotenv').config();
require('./db/main'); // Connect to MongoDB

const express = require('express');
const cors = require('cors');
const cookieParser = require('cookie-parser');

const productsRoute = require('./routes/products');
const cartRoutes    = require('./routes/cart');
const userRoutes    = require('./routes/user');   // âœ… New user route
const Cart          = require('./models/Cart');

const app = express();

// â”€â”€â”€ Middleware â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.use(cors({
  origin: 'http://localhost:5173', // your frontend port
  credentials: true               // allow cookies from frontend
}));
app.use(cookieParser());
app.use(express.json());

// â”€â”€â”€ Guest Cart Middleware â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.use(async (req, res, next) => {
  try {
    let cartId = req.cookies.cartId;

    if (!cartId) {
      const cart = await Cart.create({});
      cartId = cart._id.toString();

      res.cookie('cartId', cartId, {
        httpOnly: true,
        sameSite: 'lax',
        secure: process.env.NODE_ENV === 'production',
        maxAge: 1000 * 60 * 60 * 24 * 30 // 30 days
      });
    }

    req.cartId = cartId;
    next();
  } catch (err) {
    next(err);
  }
});

// â”€â”€â”€ Routes â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.use('/api/products', productsRoute);
app.use('/api/cart',     cartRoutes);
app.use('/api/user',     userRoutes); // âœ… User login/register routes

// â”€â”€â”€ Error Handler â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
app.use((err, req, res, next) => {
  console.error(err);
  res.status(500).json({ error: err.message || 'Internal server error' });
});

// â”€â”€â”€ Start Server â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€â”€
const PORT = process.env.PORT || 5001;
app.listen(PORT, () =>
  console.log(`ðŸš€ Server running on http://localhost:${PORT}`)
);
